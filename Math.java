public class Math {
    public static void main(String[] args) {
        double x = 3.45;
        double y = java.lang.Math.ceil(x);
        double z = java.lang.Math.floor(x);
        double k = java.lang.Math.round(x);
        double l = java.lang.Math.abs(x);
        System.out.println("Ceil of x: " + y);
        System.out.println("Floor of x: " + z);
        System.out.println("Round of x: " + k);
        System.out.println("Abs of x: " + l);
    }
    
}